// ─────────────────────────────────────────────────────────────────────────────
// FONT REGISTRY & RENDERER
// ─────────────────────────────────────────────────────────────────────────────

const FONTS = {
    standard: FONT_STANDARD,
    banner:   FONT_BANNER,
    block:    FONT_BLOCK,
    small:    FONT_SMALL,
    shadow:   FONT_SHADOW,
    slant:    FONT_SLANT,
};

/**
 * Returns the number of rows for a given font.
 * All character arrays in a font must have the same length.
 */
function fontHeight(font) {
    const sample = font["A"] || font[" "];
    return sample ? sample.length : 6;
}

/**
 * Returns the default blank character rows for a font (same width as space).
 */
function blankChar(font, rows) {
    const spaceRows = font[" "] || Array(rows).fill("  ");
    return spaceRows.map(r => " ".repeat(r.length));
}

/**
 * Renders a string using the selected font, returning multi-line ASCII art.
 */
function renderAsciiArt(text, fontName) {
    const font = FONTS[fontName] || FONTS.standard;
    const rows = fontHeight(font);
    const blank = blankChar(font, rows);

    // Limit to printable ASCII, uppercase for letter lookup
    const chars = [...text.toUpperCase()].slice(0, 60);

    if (chars.length === 0) return "";

    // Build row arrays
    const lineRows = Array.from({ length: rows }, () => []);

    for (const ch of chars) {
        const charRows = font[ch] || font[" "] || blank;
        for (let r = 0; r < rows; r++) {
            lineRows[r].push(charRows[r] !== undefined ? charRows[r] : " ".repeat(blank[0].length));
        }
    }

    // Join each row with a single space separator, then join all rows
    return lineRows.map(rowParts => rowParts.join(" ")).join("\n");
}

